    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Administrador administrador = new Administrador();
        ContaUsuario contaUsuario = new ContaUsuario();

        try {
            AdministradorService admService = new AdministradorService();

            administrador.setCpf(cpf.getText());
            administrador.setRg(rg.getText());
            administrador.setNome(nome.getText());
            administrador.setDataNasc(dataNasc.getText());
            administrador.setTelefone(telefone.getText());
            administrador.setEmail(email.getText());
            administrador.setRua(rua.getText());
            administrador.setNumCasa(numero.getText());
            administrador.setBairro(bairro.getText());
            administrador.setCidade(cidade.getText());
            String pass = new String(senha.getPassword());
            administrador.setSenha(pass);

            admService.adicionarAdministrador(administrador);

            ContaUsuarioService contaUsuarioService = new ContaUsuarioService();

            contaUsuario.setCpf(administrador.getCpf());
            contaUsuario.setSenha(administrador.getSenha());
            contaUsuario.setTipoConta(TipoConta.ADM);

            contaUsuarioService.adicionarContaUsuario(contaUsuario);

            JOptionPane.showMessageDialog(null, "Dados salvos com sucesso!");

            this.dispose();
            new TelaAdmin().setVisible(true);

        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(TelaCadastroAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

