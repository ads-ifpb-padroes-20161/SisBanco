    private void preencher() throws SQLException {
        MovimentacaoService movimentacaoService = new MovimentacaoService();
        float saque = movimentacaoService.somar("SAQUE");
        labelSaques.setText("R$:" + saque);

        float transferencia = movimentacaoService.somar("TRANSF");
        labelTransfe.setText("R$:" + transferencia);

        float deposito = movimentacaoService.somar("DEPOSITO");
        labelDepositos.setText("R$:" + deposito);

    }

