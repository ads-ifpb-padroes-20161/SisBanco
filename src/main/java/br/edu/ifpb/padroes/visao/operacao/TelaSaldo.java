    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        try {
            Conta conta;
            boolean existe = false;
            ContaService contaService = new ContaService();

            List<Cliente> clientes = contaService.buscarTitulares(this.contaCliente.getText());

            for (Cliente cliente : clientes) {
                if (this.cpfCliente.getText().equals(cliente.getCpf_cnpj())) {
                    existe = true;
                }
            }

            if (existe) {
                conta = contaService.pesquisarConta(this.contaCliente.getText());

                SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
                this.dataAbertura.setText(f.format(conta.getDataAbertura()));
                this.numAgencia.setText("" + conta.getAgencia().getNumero());
                this.valor.setText(("" + conta.getSaldo()));
            } else {
                JOptionPane.showMessageDialog(null, "Cliente não possui esta conta!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(TelaPesquisaConta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buscarActionPerformed

